package fy.gisweb.model;

public class BasePoints {
	private double x;
	private double y;
	private double v;
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getV() {
		return v;
	}
	public void setV(double v) {
		this.v = v;
	}
}
